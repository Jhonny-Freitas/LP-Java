
public class TestaTiposPrimitivos {
	

    public static void main(String[] args) {
		
		byte valorByte = 127;
        short valorShort = 32000;
        int valorInt = 1000000;
        long valorLong = 1234567890L;
        float valorFloat = 3.14f;
        double valorDouble = 2.71828;
        char valorChar = 'A';
        boolean valorBoolean = true;

        System.out.println("Valor do byte: " + valorByte);
        System.out.println("Valor do short: " + valorShort);
        System.out.println("Valor do int: " + valorInt);
        System.out.println("Valor do long: " + valorLong);
        System.out.println("Valor do float: " + valorFloat);
        System.out.println("Valor do double: " + valorDouble);
        System.out.println("Valor do char: " + valorChar);
        System.out.println("Valor do boolean: " + valorBoolean);

    } 
   
}



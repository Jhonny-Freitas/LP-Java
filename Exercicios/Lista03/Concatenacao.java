
public class Concatenacao {
	

    public static void main(String[] args) {
		
		System.out.println("Java" + 1 + 2 + 3); // Exibe: Java123
        System.out.println("Java" + (1 + 2) + 3); // Exibe: Java6
        System.out.println(1 + 2 + 3 + "Java"); // Exibe: 123Java
        System.out.println(1 + (2 + 3) + "Java"); // Exibe: 6Java
    
    } 
   
}


